-- abs()
SELECT ABS(66), ABS(- 66);

-- ceiling()
SELECT CEILING(12.34), CEILING(-12.34);

-- floor()
SELECT FLOOR(12.34), FLOOR(-12.34);

-- pi()
SELECT PI();
SELECT PI()+0.0000000000;

-- degrees()
SELECT DEGREES(PI()), DEGREES(PI()/2);

-- pow()
SELECT POW(2,2);
SELECT POW(8, -2);

-- sqrt()
SELECT SQRT(4), SQRT(64), SQRT(-256);



